a = 1                         # int data type     / int veri tipi tam sayıları tutan veri tipi
b = 1.111                     # float data type   / float ondalıklı sayılar için veri tipi
c = "Python"                  # string data type  / string yani metinsel ifadeler için veri tipi
d = ["Hello", 1, 2, 3]        # list data type    / list veri tipi adı üzerinde listeler için veri tipi
e = (1, 2, 3, "Hello")        # tuple data type   / tuple veri tipi list veri tipine benzer ancak bazı metotlar tuple'da yoktur.
f = {"Egg": 24, "Meat": 14}   # dict data type    / dict yani sözlük veri tipi
g = True                      # boolean data type / boolean veri tipi sadece iki değer alır True(Doğru) ya da False(Yanlış)

print(type(a))        # Here we print the data types using the "type" function
print(type(b))        # Burada değişken oluşturup veri tiplerini size açıkladım bu satır type fonksiyonu ile size veri tipini gösterir.
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
